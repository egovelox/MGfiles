// ==========================================================================
// Classe Point                                     
// ==========================================================================

public class Point
{

// --------------------------------------------------------------------------
// Proprietes
// --------------------------------------------------------------------------
    private int x;
    private int y;

// --------------------------------------------------------------------------
// Constructeurs
// --------------------------------------------------------------------------
    public Point()
    {
    }

    public Point(int x, int y)
    {
       this.x = x;
       this.y = y;
    }

// --------------------------------------------------------------------------
// Methodes
// --------------------------------------------------------------------------
// Getters
// --------------------------------------------------------------------------
    public int getX()
    {
       return x;
    }

    public int getY()
    {
       return y;
    }

// --------------------------------------------------------------------------
// Setters
// --------------------------------------------------------------------------
    public void setX(int x)
    {
      this.x = x;
    }

    public void setY(int y)
    {
      this.y = y;
    }

// --------------------------------------------------------------------------
// Reinitialisation
// --------------------------------------------------------------------------
    public void reinit()
    {
       this.x = 0;
       this.y = 0;
    }

    public void reinit(int x, int y)
    {
       this.x = x;
       this.y = y;
    }
}