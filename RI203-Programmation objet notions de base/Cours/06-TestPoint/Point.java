// ==========================================================================
// Classe Point                     
// ==========================================================================

public class Point
{

// --------------------------------------------------------------------------
// Proprietes
// --------------------------------------------------------------------------
    private int x;
    private int y;
    private static int nombrePoints;

// --------------------------------------------------------------------------
// Constructeur
// --------------------------------------------------------------------------
    public Point()
    {
       nombrePoints++;
    }

    public Point(int x, int y)
    {
       this.x = x;
       this.y = y;
       nombrePoints++;
    }

// --------------------------------------------------------------------------
// Methodes
// --------------------------------------------------------------------------
// Getters
// --------------------------------------------------------------------------
    public int getX()
    {
       return x;
    }

    public int getY()
    {
       return y;
    }

    public static int getNombrePoints()
    {
       return nombrePoints;
    }

// --------------------------------------------------------------------------
// Setters
// --------------------------------------------------------------------------
    public void setX(int x)
    {
      this.x = x;
    }

    public void setY(int y)
    {
      this.y = y;
    }
    
// --------------------------------------------------------------------------
// toString
// --------------------------------------------------------------------------
    public String toString()
    {
        return x + ", " + y;
    }
}
