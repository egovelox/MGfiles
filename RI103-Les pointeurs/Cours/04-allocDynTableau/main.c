/* ========================================================================== */
/* Projet : allocDyntableau -- Allocation dynamique d'un tableau de doubles   */
/* ========================================================================== */

#include <stdio.h>
#include <stdlib.h>

int main()
{
    double* tableau;
    int i;

/* -------------------------------------------------------------------------- */
/* Allocation dynamique du tableau                                            */
/* -------------------------------------------------------------------------- */
    tableau = (double*) calloc(3, sizeof(double));
    printf("tableau  = %p\n\n", tableau);

/* -------------------------------------------------------------------------- */
/* Initialisation du tableau                                                  */
/* -------------------------------------------------------------------------- */
    tableau[0] = 5.1;
    tableau[1] = 17.3;
    tableau[2] = 13.4;

/* -------------------------------------------------------------------------- */
/* Affichage du tableau                                                       */
/* -------------------------------------------------------------------------- */
    for (i = 0; i < 3; i++)
    {
        printf("tableau[%d] = %5.1f\n", i, tableau[i]);
    }

    return 0;
}
