/* ========================================================================== */
/* Projet : pointeurTableau -- Exemple de pointeur sur un tableau d'entiers   */
/* ========================================================================== */

#include <stdio.h>

int main()
{
    double* p;
    double tableau[3] = {5.1, 17.3, 13.4};

/* -------------------------------------------------------------------------- */
/* Initialisation et affichage de p                                           */
/* -------------------------------------------------------------------------- */
    p = tableau;              /* Affectation de l'adresse du tableau dans p    */
    printf("p  = %p\n\n", p);

/* -------------------------------------------------------------------------- */
/* Affichage de la valeur pointee par p                                       */
/* -------------------------------------------------------------------------- */
    printf("*p = %5.1f\n\n", *p);

/* -------------------------------------------------------------------------- */
/* Incrementation du pointeur p.                                              */
/* Affichage de p.                                                            */
/* Affichage de la valeur pointee par p.                                      */
/* -------------------------------------------------------------------------- */
    p++;
    printf("p  = %p\n\n", p);
    printf("*p = %5.1f\n\n", *p);

    return 0;
}
